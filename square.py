
def area(a):
    '''
    Calculates the area of a square.
    
        Parameters:
            a (int): side of a square.
        
        Return value:
            (int): the area of a square.

        Example:
            area(5)
            (5 * 5) = 25
            returns 25

    '''
    return a * a


def perimeter(a):
    '''
    Calculates the perimeter of a square.
    
        Parameters:
            a (int): side of a square.
        
        Return value:
            (int): the perimeter of a square.

        Example:
            perimeter(5)
            (4 * 5) = 20
            returns 20

    '''
    return 4 * a
